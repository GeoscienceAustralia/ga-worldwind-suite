package au.gov.ga.worldwind.animator.util.message;

/**
 * Constants class that contains the keys into the animation message bundle
 * 
 * @author James Navin (james.navin@ga.gov.au)
 */
public class AnimationMessageConstants
{
	/**
	 * @return The key for the default camera name
	 */
	public static String getCameraNameKey()
	{
		return "animator.animation.camera.name";
	}
	
	/**
	 * @return The key for the default camera eye latitude parameter name
	 */
	public static String getCameraEyeLatNameKey()
	{
		return "animator.animation.camera.eye.lat.name";
	}
	
	/**
	 * @return The key for the default camera eye longitude parameter name
	 */
	public static String getCameraEyeLonNameKey()
	{
		return "animator.animation.camera.eye.lon.name";
	}
	
	/**
	 * @return The key for the default camera eye zoom parameter name
	 */
	public static String getCameraEyeZoomNameKey()
	{
		return "animator.animation.camera.eye.zoom.name";
	}
	
	/**
	 * @return The key for the default camera look-at latitude parameter name
	 */
	public static String getCameraLookatLatNameKey()
	{
		return "animator.animation.camera.lookat.lat.name";
	}
	
	/**
	 * @return The key for the default camera look-at longitude parameter name
	 */
	public static String getCameraLookatLonNameKey()
	{
		return "animator.animation.camera.lookat.lon.name";
	}
	
	/**
	 * @return The key for the default camera look-at zoom parameter name
	 */
	public static String getCameraLookatZoomNameKey()
	{
		return "animator.animation.camera.lookat.zoom.name";
	}
	
}
